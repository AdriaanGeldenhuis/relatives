<div class="doc-header">
    <h2 class="doc-title">Data Processing Agreement (DPA)</h2>
    <p class="doc-updated">Last Updated: January 15, 2025</p>
</div>

<div class="doc-body">
    <p class="doc-intro">Between:</p>
    <ul>
        <li><strong>Relatives</strong> ("Processor")</li>
        <li><strong>The Family Admin / Account Owner</strong> ("Controller")</li>
    </ul>
    
    <h3>1. Purpose</h3>
    <p>This DPA governs how the Processor handles personal data on behalf of the Controller.</p>
    
    <h3>2. Processor Obligations</h3>
    <p>The Processor will:</p>
    <ul>
        <li>Process data only to provide app functionality</li>
        <li>Maintain confidentiality</li>
        <li>Implement appropriate security</li>
        <li>Notify the Controller of any significant data incidents</li>
    </ul>
    
    <h3>3. Controller Obligations</h3>
    <p>The Controller will:</p>
    <ul>
        <li>Ensure lawful basis for collecting user data</li>
        <li>Ensure all users in the family are authorized</li>
        <li>Manage deletion requests properly</li>
    </ul>
    
    <h3>4. Sub-processors</h3>
    <p>We may use:</p>
    <ul>
        <li>Hosting providers</li>
        <li>Weather API</li>
        <li>Maps API</li>
        <li>Notification services</li>
    </ul>
    <p>All sub-processors follow strict data protection standards.</p>
    
    <h3>5. Data Subject Rights</h3>
    <p>We will assist the Controller with:</p>
    <ul>
        <li>Access requests</li>
        <li>Deletion</li>
        <li>Correction</li>
    </ul>
    
    <h3>6. Termination</h3>
    <p>When the Controller deletes the family group:</p>
    <ul>
        <li>All data is permanently removed</li>
        <li>Backups are removed according to retention policies</li>
    </ul>
</div>